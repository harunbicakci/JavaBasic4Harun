package com.neotech.lesson02;

public class PrintLine {
	public static void main(String[] args) {
		
		System.out.println("What`s up Yilmaz?");
		
		System.out.println("How are you doing?");
		
		// println --> prints and then moves to the new line.
		// print --> prints and DOES NOT moves to the new line.
		
		
		System.out.print("1");
		System.out.print("2");
		System.out.print("3"); // After printing 3, it will stay at the same line.
		
		System.out.println("4"); // After printing 4, it moves to the new line.
		System.out.println("5");
		System.out.println("6");
		
		System.out.println("Do you like Java? \nYes I do!"); // here \n jumps to the next line.
	
		// for windows ctrl + shift + f --> will format your code.
		// for MAC command + shift + f --> will format your code.
	}
}
