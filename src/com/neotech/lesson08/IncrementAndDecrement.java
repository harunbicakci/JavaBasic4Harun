package com.neotech.lesson08;

public class IncrementAndDecrement {
	public static void main(String[] args) {
		
		int x = 10;
		// Re-assigning value.
		x = 11;
		
		x = x + 1;
		
		// shorthand operator
		x += 1;
		
		// increment 
		x++;
		
		System.out.println(x);
		
		int y = 50;
		
		y = y - 1;
		
		// shorthand operator
		y -= 1;
		
		// decrement
		y--;
		
		System.out.println(y);
		
		
		
		
		
		
		
	}
}
